// ─── Утилиты ─────────────────────────────────────────────────────────────────

async function post(path: string, data: unknown) {
  const res = await fetch(`${CMS_URL}/api${path}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ data }),
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`POST ${path} failed: ${res.status} ${err}`);
  }
  return res.json();
}

async function put(path: string, data: unknown) {
  const res = await fetch(`${CMS_URL}/api${path}`, {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ data }),
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`PUT ${path} failed: ${res.status} ${err}`);
  }
  return res.json();
}

async function get(path: string) {
  const res = await fetch(`${CMS_URL}/api${path}`);
  if (!res.ok) return null;
  return res.json();
}

// ─── Seed ────────────────────────────────────────────────────────────────────

async function seedSingleType(apiPath: string, data: unknown, label: string) {
  const existing = await get(apiPath) as { data?: unknown } | null;
  if (existing?.data) {
    await put(apiPath, data);
    console.log(`✓ ${label} обновлён`);
  } else {
    await post(apiPath, data);
    console.log(`✓ ${label} создан`);
  }
}

async function seedCollection(apiPath: string, items: unknown[], label: string) {
  const existing = await get(`${apiPath}?pagination[pageSize]=1`) as { meta?: { pagination?: { total?: number } } } | null;
  if ((existing?.meta?.pagination?.total ?? 0) > 0) {
    console.log(`⏭  ${label} — уже есть данные, пропускаю`);
    return;
  }
  for (const item of items) {
    await post(apiPath, item);
  }
  console.log(`✓ ${label} — добавлено ${items.length} записей`);
}

